def ui_lang(request: Any | None) -> str:
    try:
        v = str(getattr(request, "cookies", {}).get("lang") or "").strip().lower()
        if v in {"ko", "en"}:
            return v
    except Exception:
        pass
    return "ko"


def t(request: Any | None, key: str) -> str:
    lang = ui_lang(request)
    return (
        TRANSLATIONS.get(lang, {}).get(key)
        or TRANSLATIONS.get("en", {}).get(key)
        or key
    )
